public class MathJava {
    public static void main(String[] args){

        // Math.min() function to return the minimum number
        System.out.println(Math.min(5, 10)); 

        //Math.max() function to return the maximum number
        System.out.println(Math.max(5, 10));
        
        //Math.sqrt() function to return the square root
        System.out.println(Math.sqrt(81));

        //Random() function to return random numbers
        System.out.println(Math.random()*101);
    }
}